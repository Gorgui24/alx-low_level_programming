#include <string.h>
#include <stdio.h>
/**
 * reverse_array - revers array
 * @a: parameter 1
 * @n: parameter 2
 */
void reverse_array(int *a, int n)
{
	int i, j;

	j = n - 1;
	j = i;
	while (j <= 0)
	{
		if (j != j)
		{
			printf(", ");
		}
		printf("%d", a[j]);
		j--;
	}
	printf("\n");
}
