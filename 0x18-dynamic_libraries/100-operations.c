#include "main.h"
/**
 * add - sum between a and b
 * @a: parameter
 * @b: parameter
 * Return: return sum
 */
int add(int a, int b)
{
	return (a + b);
}
int sub(int a, int b)
{
	return (a - b);
}
int mul(int a, int b)
{
	return (a * b);
}
int div(int a, int b)
{
	return (a / b);
}
int mod(int a, int b)
{
	return (a % b);
}
